    public function update(Request $request, $id)
    {
        //

        $rules = [
            'name' => 'required|string|max:20',
            'email' => 'unique|string|max:64',
        ];

        $this->validate($request,$rules);
        $user = \App\User::find($id);
        $input = $request->all();
        $user->update($input);

